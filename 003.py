# Peça para o usuário digitar 3 valores inteiros e imprima a soma deles.

print('Digite o 1o. número:')
number1 = int(input())
print('Digite o 2o. número:')
number2 = int(input())
print('Digite o 3o. número:')
number3 = int(input())
total = number1 + number2 + number3
print(f'A soma dos números é: {total}')